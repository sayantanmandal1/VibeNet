export const getCountryByCode = (code) => {
  return countryCodes.find(country => country.code === code);
};

export const getPhoneCodeByCountry = (countryCode) => {
  const country = getCountryByCode(countryCode);
  return country ? country.phoneCode : '+1';
};

export const validatePhoneNumber = (phoneNumber, countryCode) => {
  if (!phoneNumber) return { isValid: true, message: '' }; // Optional field
  
  const country = getCountryByCode(countryCode);
  if (!country) return { isValid: false, message: 'Invalid country selected' };
  
  // Remove all non-digit characters except +
  const cleanPhone = phoneNumber.replace(/[^\d+]/g, '');
  
  // Check if phone starts with country code
  if (!cleanPhone.startsWith(country.phoneCode)) {
    return { 
      isValid: false, 
      message: `Phone number must start with ${country.phoneCode} for ${country.name}` 
    };
  }
  
  // Remove country code for length validation
  const localNumber = cleanPhone.substring(country.phoneCode.length);
  
  // Basic length validation (most countries have 7-15 digit local numbers)
  if (localNumber.length < 7 || localNumber.length > 15) {
    return { 
      isValid: false, 
      message: 'Phone number length is invalid' 
    };
  }
  
  // Check for valid digits only in local part
  if (!/^\d+$/.test(localNumber)) {
    return { 
      isValid: false, 
      message: 'Phone number contains invalid characters' 
    };
  }
  
  return { isValid: true, message: 'Valid phone number' };
};

export const formatPhoneNumber = (phoneNumber, countryCode) => {
  if (!phoneNumber) return '';
  
  const country = getCountryByCode(countryCode);
  if (!country) return phoneNumber;
  
  // Remove all non-digit characters except +
  const cleanPhone = phoneNumber.replace(/[^\d+]/g, '');
  
  // If doesn't start with country code, add it
  if (!cleanPhone.startsWith(country.phoneCode)) {
    // Remove any existing + or country code attempts
    const localOnly = cleanPhone.replace(/^\+?\d{1,4}/, '');
    return `${country.phoneCode}${localOnly}`;
  }
  
  return cleanPhone;
};